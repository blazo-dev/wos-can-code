/*
  Recursively sum the elements of an array of integers.
*/

const numsA = [1, 2, 3];
const expectedA = 6;

const numsB = [1];
const expectedB = 1;

const numsC = [];
const expectedC = 0;

/**
 * Recursively sums the elements in the given array.
 * - Time Complexity: O(n), where n is the length of the array.
 * - Space Complexity: O(n) due to the call stack from recursion.
 * @param {Array<number>} nums - The array of numbers to sum.
 * @returns {number} The total sum of the array elements.
 */
function recursiveSumArr(nums) {
  if(nums.length === 0) return 0;
  
  return nums.pop() + recursiveSumArr(nums)
}

console.log({recursiveSumArr: recursiveSumArr(numsA), expectedA})
console.log({recursiveSumArr: recursiveSumArr(numsB), expectedB})
console.log({recursiveSumArr: recursiveSumArr(numsC), expectedC})